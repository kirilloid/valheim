type StructuralIntegrity = {
  maxSupport: number;
  minSupport: number;
  verticalLoss: number;
  horizontalLoss: number;
};

const durabilityTable: Record<MaterialType, StructuralIntegrity> = {
  [MaterialType.Wood]: {
    maxSupport: 100,
    minSupport: 10,
    verticalLoss: 1/8,
    horizontalLoss: 1/5,
  },
  [MaterialType.HardWood]: {
    maxSupport: 140,
    minSupport: 10,
    verticalLoss: 1/10,
    horizontalLoss: 1/6,
  },
  [MaterialType.Stone]: {
    maxSupport: 1000,
    minSupport: 100,
    verticalLoss: 1/8,
    horizontalLoss: 1,
  },
  [MaterialType.Iron]: {
    maxSupport: 1500,
    minSupport: 20,
    verticalLoss: 1/13,
    horizontalLoss: 1/13,
  },
  [MaterialType.Marble]: {
    maxSupport: 1500,
    minSupport: 100,
    verticalLoss: 1/8,
    horizontalLoss: 1/2,
  },
  [MaterialType.Ashstone]: {
    maxSupport: 2000,
    minSupport: 100,
    verticalLoss: 1/10,
    horizontalLoss: 1/3,
  },
  [MaterialType.Ancient]: {
    maxSupport: 20000,
    minSupport: 100,
    verticalLoss: 0.005,
    horizontalLoss: 0.005,
  },
};

export function getStructuralIntegrity(material: MaterialType): StructuralIntegrity {
  return durabilityTable[material];
}

